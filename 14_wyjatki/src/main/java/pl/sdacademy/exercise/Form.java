package pl.sdacademy.exercise;

public class Form {

    private String name;
    private String email;
    private String address;
    private int age;

    public Form(String name, String email, String address, int age) {
        this.name = name;
        this.email = email;
        this.address = address;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getAddress() {
        return address;
    }

    public int getAge() {
        return age;
    }
}
